package lista_3.q4;

public class Wizard {
  private String nome;
  private String varinha;

  public Wizard(String nome, String varinha) {
    this.nome = nome;
    this.varinha = varinha;
  }

  public String getVarinha() {
    return varinha;
  }

  public void setVarinha(String varinha) {
    this.varinha = varinha;
  }

  public String getNome() {
    return nome;
  }

  public void setNome(String nome) {
    this.nome = nome;
  }
}
